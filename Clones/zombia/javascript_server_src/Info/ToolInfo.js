exports.getTool = (name, tier = 1) => {
    if (name === "*") return toolInfo;
    const tool = toolInfo[name];
    if (!tool) return;
    let obj = {};
    for (let i in tool) {
        if (Array.isArray(tool[i])) obj[i] = tool[i][tier - 1];
        else obj[i] = tool[i];
    }
    return obj;
}